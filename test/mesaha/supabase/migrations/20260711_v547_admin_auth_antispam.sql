create or replace function public.mesaha_backup_write_guard_v547()
returns trigger
language plpgsql
security definer
set search_path=public, auth
as $$
declare
  uid uuid := auth.uid();
  role_name text := coalesce(auth.role(), '');
  r jsonb;
  bucket text;
  allowed_limit integer;
begin
  if role_name = 'service_role' or uid is null then return new; end if;
  if tg_table_name = 'mesaha_backup_slots' then
    bucket := 'backup-slot-db:' || uid::text;
    allowed_limit := 30;
  else
    bucket := 'backup-chunk-db:' || uid::text;
    allowed_limit := 900;
  end if;
  r := public.mesaha_take_rate_limit(bucket, allowed_limit, 3600, 1800);
  if coalesce((r->>'allowed')::boolean, false) is not true then
    raise exception 'Yedek yazma sınırı aşıldı. Daha sonra tekrar deneyin.' using errcode='P0001';
  end if;
  return new;
end;
$$;
revoke all on function public.mesaha_backup_write_guard_v547() from public, anon, authenticated;
grant execute on function public.mesaha_backup_write_guard_v547() to service_role;

