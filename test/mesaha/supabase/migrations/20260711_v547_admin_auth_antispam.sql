-- Audit tablosu saldırı altında sınırsız büyümesin.
create or replace function public.mesaha_security_cleanup_v547()
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  d1 integer:=0; d2 integer:=0; d3 integer:=0; d4 integer:=0;
begin
  delete from public.mesaha_request_dedup where expires_at < now(); get diagnostics d1=row_count;
  delete from public.mesaha_rate_limits where updated_at < now()-interval '7 days'; get diagnostics d2=row_count;
  delete from public.mesaha_admin_sessions where revoked_at is not null and revoked_at < now()-interval '30 days'; get diagnostics d3=row_count;
  delete from public.mesaha_admin_audit_logs where created_at < now()-interval '180 days'; get diagnostics d4=row_count;
  return jsonb_build_object('dedup',d1,'rate',d2,'sessions',d3,'audit',d4);
end;
$$;
revoke all on function public.mesaha_security_cleanup_v547() from public, anon, authenticated;
grant execute on function public.mesaha_security_cleanup_v547() to service_role;
