create or replace function public.mesaha_rate_limit_status(p_key text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  r public.mesaha_rate_limits%rowtype;
  retry_seconds integer := 0;
begin
  select * into r from public.mesaha_rate_limits where bucket_key = p_key;
  if not found then
    return jsonb_build_object('allowed', true, 'count', 0, 'retry_after', 0);
  end if;
  if r.blocked_until is not null and r.blocked_until > now() then
    retry_seconds := greatest(1, ceil(extract(epoch from (r.blocked_until - now())))::integer);
    return jsonb_build_object('allowed', false, 'count', r.request_count, 'retry_after', retry_seconds, 'blocked_until', r.blocked_until);
  end if;
  return jsonb_build_object('allowed', true, 'count', r.request_count, 'retry_after', 0);
end;
$$;

create or replace function public.mesaha_take_rate_limit(
  p_key text,
  p_limit integer,
  p_window_seconds integer,
  p_block_seconds integer default 0
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  r public.mesaha_rate_limits%rowtype;
  now_ts timestamptz := now();
  retry_seconds integer := 0;
  next_count integer := 1;
begin
  insert into public.mesaha_rate_limits(bucket_key, window_started_at, request_count, blocked_until, updated_at)
  values (p_key, now_ts, 0, null, now_ts)
  on conflict (bucket_key) do nothing;

  select * into r from public.mesaha_rate_limits where bucket_key = p_key for update;

  if r.blocked_until is not null and r.blocked_until > now_ts then
    retry_seconds := greatest(1, ceil(extract(epoch from (r.blocked_until - now_ts)))::integer);
    return jsonb_build_object('allowed', false, 'count', r.request_count, 'retry_after', retry_seconds, 'blocked_until', r.blocked_until);
  end if;

  if r.window_started_at is null or r.window_started_at <= now_ts - make_interval(secs => greatest(1, p_window_seconds)) then
    r.window_started_at := now_ts;
    r.request_count := 0;
    r.blocked_until := null;
  end if;

  next_count := r.request_count + 1;
  if next_count > greatest(1, p_limit) then
    r.blocked_until := now_ts + make_interval(secs => greatest(1, p_block_seconds));
    update public.mesaha_rate_limits
      set request_count = next_count, blocked_until = r.blocked_until, window_started_at = r.window_started_at, updated_at = now_ts
      where bucket_key = p_key;
    retry_seconds := greatest(1, p_block_seconds);
    return jsonb_build_object('allowed', false, 'count', next_count, 'retry_after', retry_seconds, 'blocked_until', r.blocked_until);
  end if;

  update public.mesaha_rate_limits
    set request_count = next_count, blocked_until = null, window_started_at = r.window_started_at, updated_at = now_ts
    where bucket_key = p_key;
  return jsonb_build_object('allowed', true, 'count', next_count, 'retry_after', 0);
end;
$$;

create or replace function public.mesaha_reset_rate_limit(p_key text)
returns void
language sql
security definer
set search_path = public
as $$
  delete from public.mesaha_rate_limits where bucket_key = p_key;
$$;

create or replace function public.mesaha_grant_admin(p_email text, p_display_name text default null)
returns uuid
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  uid uuid;
  normalized text := lower(trim(p_email));
begin
  select id into uid from auth.users where lower(email) = normalized limit 1;
  if uid is null then
    raise exception 'Supabase Auth kullanıcısı bulunamadı: %', normalized;
  end if;
  insert into public.mesaha_admin_accounts(user_id, email, display_name, active, updated_at)
  values(uid, normalized, nullif(trim(p_display_name), ''), true, now())
  on conflict (user_id) do update set email = excluded.email, display_name = excluded.display_name, active = true, updated_at = now();
  return uid;
end;
$$;

